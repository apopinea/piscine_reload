int		ft_iterative_factorial(int nb)
{
	int i;
	int res;

	res = 1;
	if (nb >= 0 && nb <= 12)
	{
		i = 1;
		while (i <= nb)
			res = res * i++;
		return (res);
	}
	return (0);
}
